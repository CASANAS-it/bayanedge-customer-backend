
// Properties
import Properties from '../properties'

// Database
import UserModel from '../models/UserModel'

// Security
import { authorize } from '../security/SecurityManager'

import CommonMessage from '../classes/CommonMessage'
// Errors
import Errors from '../classes/Errors'
import ErrorManager from '../classes/ErrorManager'
import { ledgerService } from '../services/LedgerService'
import { UserType } from '../classes/Constants'
import { getPagination, getPagingData } from '../utils/CommonUtil'

const customControllers = {
    init: router => {
        const baseUrl = `${Properties.api}/ledger`
        router.post(baseUrl + '/get', authorize(), customControllers.get)
        router.post(baseUrl + '/save', authorize(), customControllers.save)
        router.post(baseUrl + '/', authorize(), customControllers.getById)
        router.post(baseUrl + '/delete', authorize(), customControllers.delete)
        router.post(baseUrl + '/summary', authorize(), customControllers.getSummary)
    },



    getSummary: async (req, res) => {
        try {

            const { client_id } = req.body;

            res.send(
                new CommonMessage({
                    data: await ledgerService.getSummary(client_id)
                })
            )


        } catch (err) {
            const safeErr = ErrorManager.getSafeError(err)
            res.status(safeErr.status).json(safeErr)
        }
    },
    get: async (req, res) => {
        try {

            const { pageIndex, pageSize, client_id, filter } = req.body;

            const { limit, offset } = getPagination(pageIndex, pageSize);
            const total = await ledgerService.getAllTotal(client_id, filter)
            ledgerService.getAll(limit, offset, client_id, filter).then(data => {
                const response = getPagingData(data, pageIndex, limit);
                response.subTotal = total[0].sum
                res.send(
                    new CommonMessage({
                        data: response
                    })
                )
            })

        } catch (err) {
            const safeErr = ErrorManager.getSafeError(err)
            res.status(safeErr.status).json(safeErr)
        }
    },
    getById: async (req, res) => {
        try {

            const { id } = req.body;
            res.send(
                new CommonMessage({
                    data: await ledgerService.getById(id)
                })
            )
        } catch (err) {
            const safeErr = ErrorManager.getSafeError(err)
            res.status(safeErr.status).json(safeErr)
        }
    },
    save: async (req, res) => {
        try {
            var data;
            if (!req.body.transaction_id) {
                data = await ledgerService.create(req.body)
            } else {
                data = await ledgerService.update(req.body)
            }
            res.send(
                new CommonMessage({
                    data: data
                })
            )
        } catch (err) {
            const safeErr = ErrorManager.getSafeError(err)
            res.status(safeErr.status).json(safeErr)
        }
    },
    delete: async (req, res) => {
        try {
            var data = await ledgerService.delete(req.body)

            res.send(
                new CommonMessage({
                    data: data
                })
            )
        } catch (err) {
            const safeErr = ErrorManager.getSafeError(err)
            res.status(safeErr.status).json(safeErr)
        }
    },
}

export default {
    ...customControllers
}
